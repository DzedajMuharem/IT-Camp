// OPERATORI U JS-u //

// Operatore delimo u 6 grupa:

// 1. Aritmeticke operatori; (Aritmetic operators);
// 2. Operatori dodeljivanja vrijednosti (Assignments operators);
// 3. String operatori (String operators);
// 4. Operatori poredjenja (Comparison operators);
// 5. Logicki operatori ( Logical operators);
// 6. Tip operatori

// 1. Aritmeticke operatori; (Aritmetic operators);
// 1.1 + operator sabiranja
console.log(4 + 5);
// 1.2 - op. oduzimanja
console.log(4 - 5);
// 1.3 * op. mnozenja
console.log(4 * 5);
// 1.4 / op. deljenja
console.log(8 / 2);
// 1.5 ** op. stepenovanja
console.log(2 ** 3 + 2);
// 1.6 % Modulo (ostatak deljenja dva broja)
console.log(11 % 3);
console.log(12 % 3);

// Uslov za parnost:
// i % 2 = 0
// Uslov za neparnost
// i % 2 = 1

// 1.7 ++ increment (Povecava vrijednost za 1)
let m = 5;
// postfiksni operator
m++; // x = x+1
console.log(m);
//prefiksni operator
let y = 11;
console.log(++y);

// 1.8 -- Decrement (Smanjuje vrijednost za 1)
let z = 99;
// Postfiksni operator
z--; // z = z-1
console.log(z);
// Prefiksni operator
console.log(--z);
